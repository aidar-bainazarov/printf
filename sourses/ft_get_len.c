#include "../ft_printf.h"

size_t	ft_get_len(long n)
{
	size_t	counter;

	counter = 1;
	if (n < 0)
	{
		n *= -1;
		counter++;
	}
	while ((n / 10) > 0)
	{
		counter++;
		n /= 10;
	}
	return (counter);
}
